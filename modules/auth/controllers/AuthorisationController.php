<?php

namespace app\modules\auth\controllers;

use Yii;
use yii\filters\AccessControl;
use app\modules\auth\components\Controller;
use yii\filters\VerbFilter;
use app\modules\auth\forms\LoginForm;

class AuthorisationController extends Controller
{
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }


    public function actionLogin()
    {
        if (!\Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->doLogin()) {
            if (!\Yii::$app->user->getIdentity()->isActive()) {
                return $this->goBack('cabinet/settings/confirm-email');
            }
            return $this->goBack();
        }
        return $this->render('login', [
            'model' => $model,
        ]);
    }


    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

}
